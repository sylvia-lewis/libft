int		ft_is_prime(int nb)
{
	int x;

	x = 2;
	if (nb == 0 || nb == 1)
		return (0);
	while (nb % x != 0 && x < nb)
		x++;
	if (x == nb)
		return (1);
	else
		return (0);
}
